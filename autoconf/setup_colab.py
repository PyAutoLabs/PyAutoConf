def check_jax_using_gpu(log_gpu_warning: bool = True) -> bool:
    """
    Report the devices JAX sees and return True if no GPU / TPU is available.

    Considers every device (not just the last one enumerated); an empty device
    list counts as no accelerator.
    """
    import jax

    devices = jax.devices()

    has_accelerator = False

    for d in devices:
        kind = str(d).lower()

        if "gpu" in kind or "cuda" in kind:
            print(f"GPU / CUDA detected: {d}")
            has_accelerator = True
        elif "tpu" in kind:
            print(f"TPU detected: {d}")
            has_accelerator = True
        elif "cpu" in kind:
            print(f"CPU detected: {d}")
        else:
            print(f"Other device detected: {d}")

    no_gpu = not has_accelerator

    if no_gpu and log_gpu_warning:

        logger.info(
            """
            JAX did not detect a GPU or TPU device and is using the CPU for computations.

            PyAuto runs significantly faster with a GPU, so it is recommended to reinstall
            JAX with GPU support, if you have a GPU available, by following the JAX
            installation instructions.
            """
        )

    return no_gpu


def _installed_version(top_package: str):
    """The installed version string of ``top_package``, or None if unknown."""
    try:
        from importlib.metadata import version

        return version(top_package)
    except Exception:
        return None


def _clone_workspace(workspace_repo: str, workspace_dir: str, top_package: str) -> None:
    """
    Shallow-clone the workspace, preferring the tag matching the installed
    release of ``top_package`` so the cloned configs / datasets match the
    pip-installed code; fall back to the default branch when no such tag
    exists (e.g. a dev install).
    """
    import subprocess

    if Path(workspace_dir).exists():
        print("Workspace already exists so not cloning again.")
        return

    tag = _installed_version(top_package)

    if tag is not None:
        result = subprocess.run(
            ["git", "clone", "--depth", "1", "--branch", tag, workspace_repo, workspace_dir]
        )
        if result.returncode == 0:
            return
        print(f"No workspace tag '{tag}' found; cloning the default branch instead.")

    subprocess.run(
        ["git", "clone", "--depth", "1", workspace_repo, workspace_dir], check=True
    )


def _gpu_error_message(project: str, spec: dict) -> str:
    return f"""
    No GPU detected in Google Colab. {spec["project_name"]} runs {spec["gpu_note"]} with a GPU,
    so switch GPU on in Colab settings.

    To do this:

    - Click "Runtime" in the top menu.
    - Click "Change runtime type".
    - Under "Hardware accelerator", select one of the "GPU" options available.

    You can set up Colab with a CPU (e.g. if GPUs are unavailable) by calling:

        setup_colab.setup("{project}", raise_error_if_not_gpu=False)
    """


def _colab_setup(
    *,
    project_name: str,
    workspace_repo: str,
    workspace_dir: str,
    packages: list,
    top_package: str,
    raise_error_if_not_gpu: bool,
    gpu_error_message: str,
) -> None:
    """
    Shared Google Colab setup for the PyAuto ecosystem.

    A no-op (bar a short message) when not running inside Google Colab.
    """
    import os
    import subprocess
    import sys

    try:
        import google.colab  # noqa: F401
    except ImportError:
        print(
            f"""
            You are not running in a Google Colab environment so cannot use the setup_colab() function.

            You should therefore have {project_name} installed locally in your environment already (e.g. via pip or
            conda) and can run the rest of your script normally.

            You may now continue running your script or Notebook.
            """
        )
        return

    # JAX performance tweak, applied only on Colab.
    os.environ["XLA_FLAGS"] = _XLA_FLAGS

    no_gpu = check_jax_using_gpu(log_gpu_warning=False)

    if no_gpu and raise_error_if_not_gpu:
        raise RuntimeError(gpu_error_message)

    # --- Install packages (no deps — Colab ships the scientific stack) ---
    print()
    print(f"Now Installing {project_name} and setting up Colab Environment:")

    subprocess.check_call(
        [sys.executable, "-m", "pip", "install", *packages, "--no-deps"]
    )

    _clone_workspace(workspace_repo, workspace_dir, top_package)

    # --- Configure autoconf paths ---
    from autoconf import conf

    os.chdir(workspace_dir)

    conf.instance.push(
        new_path=Path(workspace_dir) / "config",
        output_path=Path(workspace_dir) / "output",
    )

    print(
        f"""
        ***Google Colab Setup Complete, which included:***

        - Installation of {project_name} and other required packages.
        - Cloning of the workspace GitHub repository.
        - Setting up environment variables for JAX for improved performance.
        - Setting the configuration paths to the workspace config and output folders suitable for Colab.
        """
    )


def setup(
    project: str,
    raise_error_if_not_gpu: bool = False,
    workspace_dir: str = None,
) -> None:
    """
    Set up Google Colab for a PyAuto notebook repository.

    Parameters
    ----------
    project
        One of: "autofit", "autogalaxy", "autolens", "howtofit",
        "howtogalaxy", "howtolens".
    raise_error_if_not_gpu
        If True, raise instead of continuing when Colab has no GPU / TPU
        runtime enabled.
    workspace_dir
        Where to clone the workspace. Defaults to the project's Colab
        directory (``/content/<workspace>``); overridden by CI environments
        that simulate the Colab bootstrap (e.g. PyAutoHeart's verify_install
        check F), where ``/content`` is not writable.
    """
    try:
        spec = _PROJECTS[project]
    except KeyError:
        raise KeyError(
            f"Unknown Colab project '{project}'. Choose from: {sorted(_PROJECTS)}"
        )

    _colab_setup(
        project_name=spec["project_name"],
        workspace_repo=spec["workspace_repo"],
        workspace_dir=workspace_dir or spec["workspace_dir"],
        packages=spec["packages"],
        top_package=spec["top_package"],
        raise_error_if_not_gpu=raise_error_if_not_gpu,
        gpu_error_message=_gpu_error_message(project, spec),
    )


